package pratice;

import static java.lang.System.*;

public class EvenNumber {
    public static void main(String[] args) {
        int number = 54;
        if (number % 2 == 0) {
           System.out.println(number + " is Even Number");
        } else {
           System.out.println(number + " is Odd Number");
        }
    }


}
